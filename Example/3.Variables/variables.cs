using System;

namespace Variables
{

    class Variables
    {

        static void Main(string[] args)
        {

            Console.WriteLine("C# VARIABLES");

            string name = "CIHAN";
            int number = 21;
            double DoubleNumber = 5.99D;
            char character = 'C';
            bool VarBool = true;
            const double PI = 3.14;

            Console.WriteLine("string: "+ name);
            Console.WriteLine("int: " + number);
            Console.WriteLine("double: " + DoubleNumber);
            Console.WriteLine("char: " + character);
            Console.WriteLine("bool: " + VarBool);
            Console.WriteLine("const PI: " + PI);




        }

    }


}